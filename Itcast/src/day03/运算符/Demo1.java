package day03.运算符;

/*
java中的八种基本数据类型：

	整数：
		byte 、 short 、 int 、 long

	小数：  
		float double

	字符：
		char

	布尔：
		boolean 

字符串的类型： String(引用数据类型)


声明变量的方式：
	

	方式1： 数据类型  变量名 = 数据;


	方式2：数据类型  变量名1 ， 变量名2 ......；


*/
class Demo1 {


	public static void main(String[] args) {
		String str = ""; //字符串还是字符呢？  ""字符串没有内容是属于一个空字符串。
		System.out.println(str);
		System.out.println("刚才打印了一个空字符串！");
	}
}
